const generateToken = (id) => {
  return jwt.sign({ id }, process.env.JWT_SECRET, { expiresIn: "30d" });
};

// ‚úÖ NOUVELLE FONCTION : Configuration cookies s√©curis√©s
const getCookieOptions = () => {
  const isProduction = process.env.NODE_ENV === "production";

  return {
    expires: new Date(Date.now() + 30 * 24 * 60 * 60 * 1000), // 30 jours
    httpOnly: true, // üõ°Ô∏è PROTECTION XSS : Token inaccessible via JavaScript
    secure: isProduction, // üîí HTTPS obligatoire en production
    sameSite: isProduction ? "none" : "lax", // üåê Cross-origin pour Render
  };
};

